					<h2 class="FAQ__title">General Questions</h2><div class="FAQ__question_item"><div class="FAQ__question-parent">Will My New Metal Card Work?</div>
						<div class="FAQ__question-answer"><p>Yes! Your new metal card will work everywhere globally. Plus, it is a great marketing tool when potential clients see your company logo.</p>
</div>
					</div><div class="FAQ__question_item"><div class="FAQ__question-parent">Is It Safe?</div>
						<div class="FAQ__question-answer"><p>Yes! Carbon Co. Skins take the utmost care of your financial safety. We always recommend you ‘freeze’ your card before shipping it to us. We DO NOT record any personal data. Our equipment does not store any personal data, it simply transfers from A to B.</p>
</div>
					</div><div class="FAQ__question_item"><div class="FAQ__question-parent">How Long Does the Process Take?</div>
						<div class="FAQ__question-answer"><p>Send us your plastic card and we will notify you once this is received. We recommend sending this Express so you can always track it and avoid any delays. Once received we will begin production of your new custom card and it will be ready for postage within 1-2 days. From here we will send both your cards new and old back to you via your choice of shipping.</p>
</div>
					</div><div class="FAQ__question_item"><div class="FAQ__question-parent">What happens if my card is lost or stolen?</div>
						<div class="FAQ__question-answer"><p>Contact your bank ASAP. You must treat this the same as any debit or credit card that is owned by the card issuer.</p>
</div>
					</div><div class="FAQ__question_item"><div class="FAQ__question-parent">Will my card work in ATM machines?</div>
						<div class="FAQ__question-answer"><p>All of our metal cards work in ATM machines. However, it is not recommended to use metal cards in ATM machines that require the full insertion of the card into the reader. Due to the metal cards being heavier than plastic cards, some full insertion machine’s rollers may be too stiff to eject the card. We recommend you only use partial insertion devices, where you can always see part of the card, in order to remove it manually. </p>
</div>
