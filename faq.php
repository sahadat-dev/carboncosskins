</div>
					</div><div class="FAQ__question_item"><div class="FAQ__question-parent">Is my card compatible?</div>
						<div class="FAQ__question-answer"><p>We can upgrade almost any plastic card that has a chip and/or a magnetic strip. There are some rare chip types that are not compatible with our cards &#8211; please see the <a href="how-it-works.php"> How It Works </a> page. If you do order with a non-compatible chip, we are unable to offer refunds.</p>
</div>
